package main_study.The_07_29;

public class StartEx02 {

    public static void main(String[] args) {

        for (int i = 1; i<=5;i++) {

            for (int j = 1; j <= i; j++) {//별을 i 갯수만큼 찍어라
                System.out.print("*");
            }
            System.out.println();//5번 점검하고 줄바꾸고 반복

        }

    }
}




